            <p class="content-information"><b>Суть стиля стиля <span class="red-span">Black Work</span></b></p>

            <p class="content-information">Основная особенность, это конечно то, что в работе присутствуют большие пятна черного цвета, 
                которые могут быть более яркими или светлыми за счет используемых полутонов. Но при этом любая тату в  черном цвете 
                не будет относиться к блэкворку, а вот, например, черный треугольник – это уже классика блэкворка.
            </p>

            <p class="content-information">Ошибочно мнение, что блэкворк – это закрашенный черным цветом участок тела, 
                но все не так скучно, рисунки в стиле блэкворка интересны и необычны, что часто достигается за счет точечного нанесения.
            </p>

            <p class="content-information">Сочетается с другими стилями, например с надписями, дотворком, геометрией и уже 
                названным выше минимализмом. Также часто блэкворк перекликается с тату в стиле Полинезия из-за использования 
                узоров на эскизах.
            </p>
            
            <p class="content-information">Часто тату в стиле блэкворк клиенты выбирают, чтобы скрыть различные 
                неприятные дефекты кожи – шрамы, рубцы. Ведь под черными крупными узорами эти проблемы совсем незаметны. 
                Помимо этого блэкворк тату служат для перекрытия неудачных работ мастеров с руками не из того места. 
                Вот только важно понимать, что, если к выбору стиля блэкворк вы подошли неосознанно, то закрасить такой 
                рисунок в будущем будет почти нереально. Конечно, можно будет попробовать ввести под кожу белую краску, 
                но при таком решении в основном получается мутное пятно. Вариант с закрашиванием всей тату в черный мы 
                не рассматриваем, так как это будет жуть.
